// double == compares values while === compares data types

// == is used for loose comparison and it checks if values are true 
// === is used for strict comparison and it checks if both values and data types are true 


console.log('5' == 5); // true because the value is still 5 
console.log('5' === 5); // false because the first 5 is a string and the second is a number

